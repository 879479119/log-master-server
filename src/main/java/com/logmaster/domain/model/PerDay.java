package com.logmaster.domain.model;

public class PerDay {

    private int count;

    private int day;

    private double count1;

    private int hour;

    public int getHour() {
        return hour;
    }

    public void setHour(int hour) {
        this.hour = hour;
    }

    public double getCount1() {
        return count1;
    }

    public void setCount1(double count1) {
        this.count1 = count1;
    }

    public int getCount() {
        return count;
    }

    public void setCount(int count) {
        this.count = count;
    }

    public int getDay() {
        return day;
    }

    public void setDay(int day) {
        this.day = day;
    }
}
